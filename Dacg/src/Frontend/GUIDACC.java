    private void btnSalvarCadastroAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCadastroAlunoActionPerformed
        try {
            boolean add = this.lstAlunos.add(this.getCadastroAlunoFields());
            if (!add) {
                   modal.showMessageDialog(null, "Erro ao cadastrar Aluno",
                        "Alerta", JOptionPane.ERROR_MESSAGE);
                throw new Exception("Erro ao adcionar novo aluno a lista");
            } else {
                modal.showMessageDialog(null, "Aluno cadastrado com sucesso",
                        "Alerta", JOptionPane.INFORMATION_MESSAGE);
                this.setCadastroAlunoFields(new Aluno());
            }
        } catch (Exception e) {
            System.out.println("Deu ruim mano, se liga: " + e.getMessage());
        }


    }//GEN-LAST:event_btnSalvarCadastroAlunoActionPerformed

