    private boolean renderCadastroTurmasList() {
        try {
            String professores[] = new String[lstProfessores.size()];
            for (int i = 0; i < lstProfessores.size(); i++) {

                professores[i] = lstProfessores.get(i).getNome();

            }
            jComboBoxCadastroTurmasProfessor.setModel(new javax.swing.DefaultComboBoxModel<>(professores));

            String disciplinas[] = new String[lstDisciplinas.size()];
            for (int i = 0; i < lstDisciplinas.size(); i++) {

                disciplinas[i] = lstDisciplinas.get(i).getNome();

            }
            jComboBoxCadastroTurmasDisciplina.setModel(new javax.swing.DefaultComboBoxModel<>(disciplinas));
            String alunos[] = new String[lstAlunos.size()];
            for (int i = 0; i < lstAlunos.size(); i++) {

                alunos[i] = lstAlunos.get(i).getNome();

            }

            jListCadastroTurmasAlunos.setModel(new javax.swing.AbstractListModel<String>() {

                public int getSize() {
                    return alunos.length;
                }

                public String getElementAt(int i) {
                    return alunos[i];
                }
            });
            return true;

        } catch (Exception e) {
            System.out.println("Deu ruim mano, se liga: " + e.getMessage());
            return false;

        }
    }

